
public class Aluno {
	String	nome;
	String	idade;
	Aluno	proximo;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getIdade() {
		return idade;
	}
	public void setIdade(String idade) {
		this.idade = idade;
	}
	public Aluno getProximo() {
		return proximo;
	}
	public void setProximo(Aluno proximo) {
		this.proximo = proximo;
	}

	
}
